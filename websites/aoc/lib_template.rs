#![allow(dead_code)]

fn part_1(input: &str) -> u32 {
    input.lines().map(|line| {}).sum()
}

fn part_2(input: &str) -> u32 {
    input.lines().map(|line| {}).sum()
}

#[test]
fn test_day_n() {
    let sample = "";
    let input = include_str!("input.txt");

    assert_eq!(part_1(sample), 0);
    assert_eq!(part_1(input), 0);
    assert_eq!(part_2(sample), 0);
    assert_eq!(part_2(input), 0);
}
